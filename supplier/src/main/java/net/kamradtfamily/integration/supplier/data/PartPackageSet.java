package net.kamradtfamily.integration.supplier.data;

import java.io.Serializable;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.ManyToOne;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author randalkamradt
 */
@Entity
@XmlRootElement
public class PartPackageSet implements Serializable {
    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private Long id;
    @ManyToOne
    private PartPackage partPackage;
    private int quantity;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof PartPackageSet)) {
            return false;
        }
        PartPackageSet other = (PartPackageSet) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "net.kamradtfamily.integration.supplier.data.PartPackageSet[ id=" + id + " ]";
    }

    /**
     * @return the partPackage
     */
    public PartPackage getPartPackage() {
        return partPackage;
    }

    /**
     * @param partPackage the partPackage to set
     */
    public void setPartPackage(PartPackage partPackage) {
        this.partPackage = partPackage;
    }

    /**
     * @return the quantity
     */
    public int getQuantity() {
        return quantity;
    }

    /**
     * @param quantity the quantity to set
     */
    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
    
}
